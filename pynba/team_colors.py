def format_RGB_string(color):
    color = color.split(',')
    color[0] = color[0][1:]
    color[2] = color[2][:-1]
    return color


# "(R, G, B)" => ['R', 'G', 'B']
def RGB_to_ANSI(fore, back):
    return "\033[1m" + "\033[38;2;{};{};{};".format(fore[0], fore[1], fore[2])\
                     + "48;2;{};{};{}m".format(back[0], back[1], back[2])


